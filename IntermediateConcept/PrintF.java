package IntermediateConcept;

public class PrintF {
    public static void main(String[] args) {
        //printf 
        int a=10000;
        String w="Sathyan";
        char c='a';
        boolean b=true;
        double d=72.10000;
        System.out.printf("%,d is a number\n",a);
        System.out.printf("name is %10s\n",w);
        System.out.printf("%c is just\n",c);
        System.out.printf("%.2f",d);
        System.out.printf("%b",b);
    }
    
}
